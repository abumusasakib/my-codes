#include <iostream>
using namespace std;

int main()
{
    cout << "(" << (char) 92 << "__/)" << endl;                     //"(char) 92" is the representation of \ sign
    cout << "(='.'=)" << endl;
    cout << "C(" << (char) 34 << ")_(" << (char) 34 << ")" << endl; // (char) 34 is " sign
    cout << endl;

    cout <<"\t" << ",___," << endl;
    cout <<"\t" << "(6v6)" << endl;
    cout <<"\t" << "(_^(_" << (char) 92 << endl;
    cout <<"\t" << (char) 34 << " " << (char) 34 << " " << (char) 92 << (char) 92 << endl;
    cout << endl;

    cout << (char) 34 << "/" << (char) 92 << "_" << "/" << (char) 92 << endl;
    cout << " >^,^<" << endl;
    cout << (char) 34 << (char) 34 << "/ " << (char) 92 << endl;
    cout << "'(___)_/" << endl;
    cout << endl;

    cout <<"\t" << "   @..@" << endl;
    cout <<"\t" << "  (----)" << endl;
    cout <<"\t" << " ( >__< )" << endl;
    cout <<"\t" << " ^^ ~~ ^^" << endl;
}
